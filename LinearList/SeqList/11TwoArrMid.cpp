/*
【题目完整原文 2011统考真题 题号11】
中位数定义：长度L的升序序列S，中位数为第⌈L/2⌉个位置元素。
例：S1=(11,13,15,17,19)，L=5，⌈5/2⌉=3，中位数15；
S2=(2,4,6,8,20)，L=5，中位数6；两序列合并[2,4,6,8,11,13,15,17,19,20]总长10，⌈10/2⌉=5，第5位是11。
给定两个等长升序有序数组A、B，设计高效算法，求两个序列合并后的中位数。
要求：
1）给出算法基本设计思想；
2）C++代码+关键注释；
3）分析时间、空间复杂度。

【1.算法设计思想（课本标准答案思路）】
分别取A、B当前区间中位数a、b：
① 若a==b，该值就是整体中位数，直接返回；
② 若a<b：舍弃A左半区间、B右半区间（舍弃长度相同）；
③ 若a>b：舍弃A右半区间、B左半区间（舍弃长度相同）；
循环缩小区间，直到两数组仅剩各1个元素，取二者较小值即为答案。

【3.复杂度说明】
时间复杂度：O(log₂n)，每次折半区间；
空间复杂度：O(1)，仅用下标变量，原地操作。
*/
#include<iostream>
using namespace std;

// 王道官方原版函数：M_Search
int M_Search(int A[],int B[],int n)
{
    // s1 d1：A当前区间左右下标；s2 d2：B当前区间左右下标
    int s1=0,d1=n-1,m1;
    int s2=0,d2=n-1,m2;

    // 循环终止条件：两个数组都只剩单个元素（首尾下标重合）
    while(s1 != d1 || s2 != d2)
    {
        m1=(s1+d1)/2; // A当前区间中点下标
        m2=(s2+d2)/2; // B当前区间中点下标

        // 情况1：两中位数相等，直接返回结果
        if(A[m1]==B[m2])
            return A[m1];

        // 情况2：A中位数 < B中位数 → 删A左、删B右
        if(A[m1]<B[m2])
        {
            // 区间元素总数【奇数】：中点本身保留
            if((d1-s1+1)%2==1)
            {
                s1=m1;    // A从mid开始，舍弃mid左侧
                d2=m2;    // B到mid截止，舍弃mid右侧
            }
            // 区间元素总数【偶数】：中点舍弃
            else
            {
                s1=m1+1;  // A舍弃mid及左边
                d2=m2;    // B舍弃mid右边
            }
        }
        // 情况3：A中位数 > B中位数 → 删A右、删B左
        else
        {
            // 区间奇数个元素
            if((d1-s1+1)%2==1)
            {
                d1=m1;    // A舍弃mid右侧
                s2=m2;    // B舍弃mid左侧
            }
            // 区间偶数个元素
            else
            {
                d1=m1;    // A舍弃mid右边
                s2=m2+1;  // B舍弃mid及左边
            }
        }
    }
    // 仅剩各一个元素，返回更小值
    return A[s1]<B[s2]?A[s1]:B[s2];
}

int main()
{
    // 题目标准测试用例
    int n=4;
    int A[]={1,3,5,7};
    int B[]={2,4,6,8};
    int res=M_Search(A,B,n);
    cout<<"合并后中位数 = "<<res<<endl;
    /*运行输出：合并后中位数 = 11（和原题答案一致）*/
    return 0;
}
/*====================【复杂度注释】====================
时间复杂度：O(log₂n)，每次区间长度折半，二分次数logn；
空间复杂度：O(1)，仅定义若干下标变量，无额外数组/递归栈；
=======================================================*/